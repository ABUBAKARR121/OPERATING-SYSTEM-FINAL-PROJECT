#include "login.h"
#include "../drivers/display/vga.h"
#include "../drivers/input/keyboard.h"
#include "../../libc/string.h"

#define USERNAME "sleos"
#define PASSWORD "1234"

int login_prompt(void) {
    char user[32], pass[32];
    while (1) {
        vga_write_color("\n  \xC9\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xBB\n", 0x09);
        vga_write_color("  \xBA          SLeOS LOGIN            \xBA\n", 0x09);
        vga_write_color("  \xC8\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xBC\n\n", 0x09);
        vga_write("  Username: ", VGA_WHITE, VGA_BLACK);
        keyboard_read_line(user, 32);
        vga_write("  Password: ", VGA_WHITE, VGA_BLACK);
        keyboard_read_line(pass, 32);
        if (strcmp(user, USERNAME) == 0 && strcmp(pass, PASSWORD) == 0) {
            vga_write_color("\n  \xC9\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xBB\n", VGA_GREEN);
            vga_write_color("  \xBA     Login successful!          \xBA\n", VGA_GREEN);
            vga_write_color("  \xC8\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xBC\n\n", VGA_GREEN);
            return 1;
        } else {
            vga_write_color("\n  \xC9\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xBB\n", VGA_RED);
            vga_write_color("  \xBA   Invalid credentials!         \xBA\n", VGA_RED);
            vga_write_color("  \xC8\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xCD\xBC\n\n", VGA_RED);
        }
    }
}
